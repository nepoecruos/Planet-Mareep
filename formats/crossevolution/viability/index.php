                <h2>Viability Rankings</h2>
                <p>
                    Welcome to the Cross Evolution Viability Rankings. The original one was very inconsistent, inaccurate and had unviable Pok&eacute;mon ranked such as Mega Salamence, Arceus-Fighting, Arceus-Ice, Mega Blaziken, Pheromosa, Tailow, Floette, Salandit, Yanma, Mega Lucario, Doduo, Pancham, Swirlix, Lairon, Sliggoo and Hakamo-o. 
<br>
<br>
They had incorrect order such as Arceus-Fairy being ranked higher than Primal Groudon, Archen being B rank, Pheromosa, Marshadow, Togetic and Fraxure being higher than Kadabra, Krabby, Diglett, Clauncher and Aipom, Type Null being ranked lower than Gligar and more.
<br>
<br>
Overall, the Generation 7 Viability Rankings made by dsm77773 and shrektacularshrek in February of 2017 were the previously mentioned remarks (inconsistent, inaccurate). Now it is even outdated due to the stat restriction unban, previously if a Pokemon's stat went over 255 or under 1 then they are unusable. However, now they just stop at 255 and 1 automatically. So Chansey has access to many more Cross Evolutions, versus the previous rules of Cross Evolution since it's HP was too high only Pok&eacute;mon who gained 5 HP or 0 HP could cross evolve with it.
<br>
<br>
So, perf/snoth also known as the coder of this entire website and author of everything on this site you read has decided to remaster the previous Viability Rankings and make it significantly better since he and Timezone are the new leaders of Cross Evolution.
